/*  #2: Doubles (decimal numbers)
 *  
 *  Integers are great, but sometimes you need to use decimal numbers.
 *  In Java, we call these doubles (because they're double precision).
 *  You can do math with them too!
 *  
 *  To declare a double, type "double" and then the name of the variable.
 */
public class Doubles {
    public static void main (String []args){
        // double variable declaration (making it exist)
        double pi;
        
        // assignment (giving it a value)
        pi = 3.14159265;
                
        // And now we can use it! We can print our variable like this
        System.out.println(pi);
        System.out.println("Printing pi again: " + pi); //This is how you print the number and words with it!
    }

}
