/*  #3: Booleans
 *  This one isn't math lol
 *  Booleans are true or false values.
 *  Literally just 'yes' or 'no', 'on' or 'off', '1' or '0'.
 *  
 *  To declare a boolean, type "boolean" and then the name of the variable.
 */
public class Booleans {
    public static void main(String []args){
        boolean intsCanHoldDecimals = false;
        boolean intsCanHoldWholeNumbers = true;

        System.out.println("Ints can hold decimals like 2.5: " + intsCanHoldDecimals);
        System.out.println("Ints can hold whole numbers like 2: " + intsCanHoldWholeNumbers);

        //  This is all stuff you just learned but now with booleans
        //  They are really useful for if statements, which you'll learn about later!
    }
}
