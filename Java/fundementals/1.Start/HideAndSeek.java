/*  #2 The difference between print and println is that println will print the statement-
 *  -and then go to the next line.
 *  While print will print the statement and stay on the same line.
 * 
 * 
 * 
 */
public class HideAndSeek {
  public static void main(String[] args) {
    System.out.println("Let's play hide and seek.");

    System.out.print("Three...");
    System.out.print("Two...");

    System.out.println("One...");
    System.out.println("Ready or not, here I come!");
  }
}